/*
数字和字母的段码定义：

数字：
0 = 0x3F = 0b00111111
1 = 0x06 = 0b00000110
2 = 0x5B = 0b01011011
3 = 0x4F = 0b01001111
4 = 0x66 = 0b01100110
5 = 0x6D = 0b01101101
6 = 0x7D = 0b01111101
7 = 0x07 = 0b00000111
8 = 0x7F = 0b01111111
9 = 0x6F = 0b01101111

字母：
A = 0x77 = 0b01110111
b = 0x7C = 0b01111100
C = 0x39 = 0b00111001
d = 0x5E = 0b01011110
E = 0x79 = 0b01111001
F = 0x71 = 0b01110001
H = 0x76 = 0b01110110
L = 0x38 = 0b00111000
M = 0x37 = 0b00110111 (近似)
P = 0x73 = 0b01110011
U = 0x3E = 0b00111110

特殊符号：
- (横线) = 0x40 = 0b01000000
. (点) = 0x80 = 0b10000000
空白 = 0x00 = 0b00000000

段位定义 (Bit 7-0)：
Bit 7: DP (小数点)
Bit 6: G  (中间横段)
Bit 5: F  (左上竖段)
Bit 4: E  (左下竖段)
Bit 3: D  (底部横段)
Bit 2: C  (右下竖段)
Bit 1: B  (右上竖段)
Bit 0: A  (顶部横段)

七段显示器形状：
     A
    ---
 F |   | B
    -G-
 E |   | C
    ---
     D   .DP
*/

